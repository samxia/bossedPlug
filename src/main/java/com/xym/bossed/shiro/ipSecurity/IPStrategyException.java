package com.xym.bossed.shiro.ipSecurity;

import org.apache.shiro.authc.AuthenticationException;


public class IPStrategyException extends AuthenticationException {

	private static final long serialVersionUID = 10L;

	public IPStrategyException() {
		super();
	}

	public IPStrategyException(String message, Throwable cause) {

		super(message, cause);

	}

	public IPStrategyException(String message) {

		super(message);

	}

	public IPStrategyException(Throwable cause) {

		super(cause);
	}

}
