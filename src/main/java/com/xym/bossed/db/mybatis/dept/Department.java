package com.xym.bossed.db.mybatis.dept;

import java.io.Serializable;

public class Department implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 454121352265548761L;
	private int recoNo;
	private String name, beiZhu, danwMC, danwDM;

	public int getRecoNo() {
		return recoNo;
	}

	public void setRecoNo(int recoNo) {
		this.recoNo = recoNo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBeiZhu() {
		return beiZhu;
	}

	public void setBeiZhu(String beiZhu) {
		this.beiZhu = beiZhu;
	}

	public String getDanwMC() {
		return danwMC;
	}

	public void setDanwMC(String danwMC) {
		this.danwMC = danwMC;
	}

	public String getDanwDM() {
		return danwDM;
	}

	public void setDanwDM(String danwDM) {
		this.danwDM = danwDM;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

}
