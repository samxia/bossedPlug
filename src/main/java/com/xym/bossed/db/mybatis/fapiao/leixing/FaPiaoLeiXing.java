package com.xym.bossed.db.mybatis.fapiao.leixing;

import java.io.Serializable;
import java.math.BigDecimal;

public class FaPiaoLeiXing implements Serializable{

	private static final long serialVersionUID = -446787635324216811L;
	
	private int recoNo;
	private String Name;
	private BigDecimal shuiLv, fpDef;

	public int getRecoNo() {
		return recoNo;
	}

	public void setRecoNo(int recoNo) {
		this.recoNo = recoNo;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public BigDecimal getShuiLv() {
		return shuiLv;
	}

	public void setShuiLv(BigDecimal shuiLv) {
		this.shuiLv = shuiLv;
	}

	public BigDecimal getFpDef() {
		return fpDef;
	}

	public void setFpDef(BigDecimal fpDef) {
		this.fpDef = fpDef;
	}

}
